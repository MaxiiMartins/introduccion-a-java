package ejercicio1;

/**
 *
 * @author defaultuser0
 */
public class Producto {

    private int codigo;
    private int stock;
    private String descripcion;
    private double precio;

    public Producto() {
    }

    public Producto(int codigo, int stock, String descripcion, double precio) {
        this.codigo = codigo;
        this.stock = stock;
        this.descripcion = descripcion;
        this.precio = precio;
    }

    public int getCodigo() {
        return codigo;
    }

    public void setCodigo(int codigo) {
        this.codigo = codigo;
    }

    public int getStock() {
        return stock;
    }

    public void setStock(int stock) {
        this.stock = stock;
    }

    public String getDescripcion() {
        return descripcion;
    }

    public void setDescripcion(String descripcion) {
        this.descripcion = descripcion;
    }

    public double getPrecio() {
        return precio;
    }

    public void setPrecio(double precio) {
        this.precio = precio;
    }

}
